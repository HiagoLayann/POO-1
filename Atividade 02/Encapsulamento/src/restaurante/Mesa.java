package restaurante;

/**
 *
 * @author felip
 */
public class Mesa {
    
    private String materialUsado;
    private String valor;
    private String construtor;
    private String diametro;

    /**
     * @return the materialUsado
     */
    String getMaterialUsado() {
        return materialUsado;
    }

    /**
     * @param materialUsado the materialUsado to set
     */
    void setMaterialUsado(String materialUsado) {
        this.materialUsado = materialUsado;
    }

    /**
     * @return the valor
     */
    String getValor() {
        return valor;
    }

    /**
     * @param valor the valor to set
     */
    void setValor(String valor) {
        this.valor = valor;
    }

    /**
     * @return the construtor
     */
    String getConstrutor() {
        return construtor;
    }

    /**
     * @param construtor the construtor to set
     */
    void setConstrutor(String construtor) {
        this.construtor = construtor;
    }

    /**
     * @return the diametro
     */
    String getDiametro() {
        return diametro;
    }

    /**
     * @param diametro the diametro to set
     */
    void setDiametro(String diametro) {
        this.diametro = diametro;
    }
    
    
    
    
}
