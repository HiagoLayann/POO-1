package restaurante;

/**
 *
 * @author felip
 */
public class Talheres {

    private String alloy;
    private String ferreiro;
    private String tamanho;
    private String cor;

    /**
     * @return the alloy
     */
    String getAlloy() {
        return alloy;
    }

    /**
     * @param alloy the alloy to set
     */
    void setAlloy(String alloy) {
        this.alloy = alloy;
    }

    /**
     * @return the ferreiro
     */
    String getFerreiro() {
        return ferreiro;
    }

    /**
     * @param ferreiro the ferreiro to set
     */
    void setFerreiro(String ferreiro) {
        this.ferreiro = ferreiro;
    }

    /**
     * @return the tamanho
     */
    String getTamanho() {
        return tamanho;
    }

    /**
     * @param tamanho the tamanho to set
     */
    void setTamanho(String tamanho) {
        this.tamanho = tamanho;
    }

    /**
     * @return the cor
     */
    String getCor() {
        return cor;
    }

    /**
     * @param cor the cor to set
     */
    void setCor(String cor) {
        this.cor = cor;
    }

}
